  // ðŸ”„ FUNCIÃ“N PARA OBTENER MARCAS
  export const getBrandsByCategory = (mainCategory, subCategory = null) => {
    if (!mainCategory || !BRANDS_DATABASE[mainCategory]) {
      console.warn(`CategorÃ­a "${mainCategory}" no encontrada`);
      return [];
    }
  
    const categoryBrands = BRANDS_DATABASE[mainCategory];
  
    if (subCategory && categoryBrands[subCategory]) {
      return categoryBrands[subCategory];
    }
  
    // Si no hay subcategorÃ­a especÃ­fica, devolver todas las marcas de la categorÃ­a
    const allBrands = Object.values(categoryBrands).flat();
    return [...new Set(allBrands)]; // Eliminar duplicados
  };
  
  // ðŸ” FUNCIÃ“N PARA BUSCAR MARCAS
  export const searchBrands = (query, mainCategory = null) => {
    let brands = [];
  
    if (mainCategory && BRANDS_DATABASE[mainCategory]) {
      const categoryBrands = Object.values(BRANDS_DATABASE[mainCategory]).flat();
      brands = [...new Set(categoryBrands)];
    } else {
      // Buscar en todas las categorÃ­as
      Object.values(BRANDS_DATABASE).forEach(category => {
        Object.values(category).forEach(subCategoryBrands => {
          brands.push(...subCategoryBrands);
        });
      });
      brands = [...new Set(brands)];
    }
  
    return brands.filter(brand => 
      brand.toLowerCase().includes(query.toLowerCase())
    ).slice(0, 20); // Limitar resultados
  };